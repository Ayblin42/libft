#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i] && n > 0 && s1[i] && s2[i])
	{
		n--;
		i++;
	}
	if (n > 0)
		return ((s1[i]) - (s2[i]));
	else
		return (0);
}
